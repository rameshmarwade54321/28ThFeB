package Array;

public class Ex03_char_array 
{
	public static void main(String[] args) 
	{
		
	
			char [] ar=new char [2];
			ar[0]='A';
			ar[1]='C';
			
			System.out.println(ar[1]);
			System.out.println(ar.length);
			System.out.println("---------------------");
			
			for (int i=0; i<=ar.length-1; i++)
			{
				System.out.println(ar[i]);
			}
			
			
			
			
			
	}
}
