package control_statements;

public class example19_switch3 
{
	public static void main(String[] args) 
	{
		String Amazon="Orders";
		switch(Amazon)
		{
		case "purchase" : System.out.println("select purchase");
		break;
		case "category": System.out.println("select category");
		break;
		case "Orders" : System.out.println("select orders");
		break;
		
		}
	}
}
