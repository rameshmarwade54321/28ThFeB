package control_statements;

public class Example16_switch1 {
public static void main(String[] args) 
{
	int  input = 6;
	switch(input)
	

	{
	case 1 : System.out.println("BT");
	break;
	
	case 2 : System.out.println("AS");
	break;
	
	case 3 : System.out.println("MS");
	break;
	
	case 4 : System.out.println("LS");
	break;
	
	case 5 : System.out.println("CB");
	 break;
	 
	case 6 : System.out.println("NEFT");
	break;
	
//	default: System.out.println("invalid number");
	
	
	}
			
	
}
}
