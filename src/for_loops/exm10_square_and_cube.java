package for_loops;

public class exm10_square_and_cube 
{
	public static void main(String[] args) 
	{
		System.out.println("Square of number 1 to 5");
		for (int i=1; i<=5; i++)
		{
			System.out.println(i*i);
		}
		System.out.println("-------------------------");
		System.out.println("cube of number 1 to 5");
		for (int i=1; i<=5; i++)
		{
			System.out.println(i*i*i);
		}
	}
}
